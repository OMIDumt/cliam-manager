interface Props {
  open: boolean;
  onClose: () => void;
  onPick: (prompt: string) => void;
}

const PromptLibraryModal: React.FC<Props> = ({ open, onClose, onPick }) => {
  const [openIdx, setOpenIdx] = useState<number | null>(0);
  const [q, setQ] = useState('');

  const filtered = useMemo(() => {
    const term = q.trim();
    if (!term) return PROMPT_GROUPS;
    return PROMPT_GROUPS
      .map((g) => ({ ...g, prompts: g.prompts.filter((p) => p.includes(term) || g.title.includes(term)) }))
      .filter((g) => g.prompts.length > 0);
  }, [q]);

  if (!open) return null;
  return (
    <div className="fixed inset-0 z-[70] flex items-start justify-center px-3 py-6 overflow-y-auto" onClick={onClose}>
      <div className="absolute inset-0 bg-slate-900/70 backdrop-blur-sm" />
      <div
        onClick={(e) => e.stopPropagation()}
        className="relative w-full max-w-xl my-auto rounded-3xl bg-white shadow-2xl border border-slate-200 flex flex-col max-h-[90vh]"
        dir="rtl"
      >
        <div className="sticky top-0 bg-white/95 backdrop-blur border-b border-slate-200 px-5 py-4 rounded-t-3xl">
          <div className="flex items-center justify-between mb-2">
            <h3 className="text-base font-black text-slate-900">پرامپت‌ها</h3>
            <button onClick={onClose} className="w-8 h-8 rounded-full hover:bg-slate-100 text-slate-600 text-lg leading-none">×</button>
          </div>
          <p className="text-xs text-slate-500 text-center mb-3">می‌توانید از این سوالات برای شروع گفتگو استفاده کنید</p>
          <input
            type="text"
            value={q}
            onChange={(e) => setQ(e.target.value)}
            placeholder="جستجو در پرامپت‌ها..."
            className="w-full px-4 py-2 rounded-xl border border-slate-200 bg-slate-50 focus:bg-white focus:outline-none focus:ring-2 focus:ring-indigo-300 text-sm"
          />
        </div>

        <div className="overflow-y-auto p-4 space-y-2">
          {filtered.length === 0 && (
            <div className="text-center text-sm text-slate-500 py-8">موردی یافت نشد.</div>
          )}
          {filtered.map((g, i) => {
            const isOpen = q.trim() ? true : openIdx === i;
            return (
              <div key={g.title} className="rounded-xl border border-indigo-100 overflow-hidden">
                <button
                  onClick={() => setOpenIdx(isOpen && !q.trim() ? null : i)}
                  className="w-full flex items-center justify-between px-4 py-3 bg-indigo-50/60 hover:bg-indigo-100/60 text-right"
                >
                  <span className="text-indigo-500 text-sm">»</span>
                  <span className="font-bold text-sm text-slate-900">{g.title}</span>
                </button>
                {isOpen && (
                  <ul className="bg-white">
                    {g.prompts.map((p, j) => (
                      <li key={j} className="flex items-start gap-2 px-4 py-2.5 border-t border-slate-100 hover:bg-slate-50">
                        <button
                          onClick={() => { onPick(p); onClose(); }}
                          className="shrink-0 w-6 h-6 rounded-full bg-indigo-500 text-white text-sm font-bold flex items-center justify-center hover:bg-indigo-600"
                          aria-label="انتخاب"
                        >+</button>
                        <span className="text-xs text-slate-700 leading-relaxed text-right flex-1">{p}</span>
                      </li>
                    ))}
                  </ul>
                )}
              </div>
            );
          })}
        </div>
      </div>
    </div>
  );
};

export default PromptLibraryModal;
